public class AirthmaticOp {
    public static void main(String[] args) {
        int a = 8;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println((double) a/b);
        System.out.println(a%b);

        double x = 2.456;
        double y = 1.233;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}